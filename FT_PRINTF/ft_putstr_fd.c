#include "ft_printf.h"

int	ft_putstr_fd_r(char *s)
{
	int	i;
	int	c;

	i = 0;
	c = 0;
	if (!s)
	{
		ft_putstr_fd_r("(null)");
		return (6);
	}
	while (s[i])
	{
		c += ft_putchar_fd_r(s[i]);
		i++;
	}
	return (c);
}
